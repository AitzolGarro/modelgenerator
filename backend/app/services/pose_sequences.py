def get_pose_sequence(
    animation_type: str,
    canvas_size: tuple[int, int] = (512, 768),
) -> list[np.ndarray]:
    """Return the list of (18, 3) keypoint arrays for the given animation type.

    Keypoints are scaled from the canonical 512×768 canvas to `canvas_size`.
    Each keypoint is [x, y, confidence] where confidence ∈ {0.0, 1.0}.

    Args:
        animation_type: One of the 8 valid animation types.
        canvas_size: (width, height) of the target canvas.

    Returns:
        List of (18, 3) float32 numpy arrays.

    Raises:
        ValueError: If animation_type is not a valid type.
    """
    if animation_type not in VALID_ANIMATIONS:
        valid = ", ".join(sorted(VALID_ANIMATIONS))
        raise ValueError(
            f"Unknown animation type: {animation_type!r}. "
            f"Valid types: {valid}"
        )

    # Rotation types (rotate_left, rotate_right, turnaround) use Wan2.1 multi-view
    # generation and have no pre-defined OpenPose sequence.  Fall back to idle poses
    # so callers that need a sequence for rotation animations get a reasonable default.
    if animation_type not in _SEQUENCES:
        frames = _SEQUENCES["idle"]
    else:
        frames = _SEQUENCES[animation_type]
    target_w, target_h = canvas_size
    scale_x = target_w / _CANONICAL_W
    scale_y = target_h / _CANONICAL_H

    if scale_x == 1.0 and scale_y == 1.0:
        # No scaling needed — return copies to avoid mutation
        return [kp.copy() for kp in frames]

    scaled = []
    for kp in frames:
        scaled_kp = kp.copy()
        scaled_kp[:, 0] *= scale_x   # x
        scaled_kp[:, 1] *= scale_y   # y
        # confidence (column 2) unchanged
        scaled.append(scaled_kp)
    return scaled


def render_pose_skeleton(
    keypoints: np.ndarray,
    canvas_size: tuple[int, int],
) -> Image.Image:
    """Render an OpenPose-style skeleton on a black background.

    Draws colored limb lines and white keypoint circles.

    Args:
        keypoints: (18, 3) float32 array [x, y, confidence].
                   Keypoints with confidence == 0 are not drawn.
        canvas_size: (width, height) of the output image.

    Returns:
        RGB PIL Image of size canvas_size with skeleton drawn.
    """
    w, h = canvas_size
    img = Image.new("RGB", (w, h), (0, 0, 0))
    draw = ImageDraw.Draw(img)

    # Draw limb lines
    for limb_idx, (a, b) in enumerate(SKELETON_LIMBS):
        x_a, y_a, conf_a = keypoints[a]
        x_b, y_b, conf_b = keypoints[b]
        if conf_a > 0 and conf_b > 0:
            color = LIMB_COLORS[limb_idx]
            draw.line(
                [(int(round(x_a)), int(round(y_a))),
                 (int(round(x_b)), int(round(y_b)))],
                fill=color,
                width=3,
            )

    # Draw keypoint circles
    radius = 4
    for x, y, conf in keypoints:
        if conf > 0:
            xi, yi = int(round(x)), int(round(y))
            draw.ellipse(
                [(xi - radius, yi - radius), (xi + radius, yi + radius)],
                fill=(255, 255, 255),
            )

    return img


# ── Animation type detection ──────────────────────────────────────────────────

_KEYWORD_MAP: Final[list[tuple[str, str]]] = [
    # Rotation types (Phase 3 — must come before generic "run"/"walk" to avoid mis-match)
    (r"\b(turnaround|turn.?around|360|full.?turn|dar.?vuelta)\b", "turnaround"),
    (r"\b(rotate.?left|spin.?left|turn.?left|girar.?izquierda|left.?turn)\b", "rotate_left"),
    (r"\b(rotate.?right|spin.?right|turn.?right|girar.?derecha|right.?turn)\b", "rotate_right"),
    (r"\b(rotate|spin|spinning|rotating|girar|giro)\b", "rotate_left"),
    # Generic "turn" (without direction or "around") → turnaround as safest default
    (r"\b(turn|turning)\b", "turnaround"),
    # Standard animation types
    (r"\b(run|sprint|dash|corriendo|correr)\b", "run"),
    (r"\b(walk|walking|caminar|caminando|march)\b", "walk"),
    (r"\b(attack|swing|slash|punch|golpe|ataque)\b", "attack"),
    (r"\b(jump|leap|hop|salto|saltar)\b", "jump"),
    (r"\b(dance|dancing|baile|bailar|groove)\b", "dance"),
    (r"\b(wave|waving|greeting|saludar)\b", "wave"),
    (r"\b(hurt|pain|damage|hit|da[ñn]o|golpeado)\b", "hurt"),
    (r"\b(idle|stand|standing|breath|rest|esperar)\b", "idle"),
]


def detect_animation_type(prompt: str) -> str:
    """Detect animation type from a text prompt. Falls back to 'idle'.

    Args:
        prompt: Text description of the desired animation.

    Returns:
        One of the valid animation type strings.
    """
    lower = prompt.lower()
    for pattern, anim_name in _KEYWORD_MAP:
        if re.search(pattern, lower):
            return anim_name
    return "idle"
