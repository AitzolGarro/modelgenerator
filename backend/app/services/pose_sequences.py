# ── Canonical pose data (512 × 768) ──────────────────────────────────────────
# fmt: off

def _kp(*rows: tuple) -> np.ndarray:
    """Build an (18, 3) float32 keypoint array from 18 (x, y, conf) tuples."""
    assert len(rows) == 18, f"Expected 18 keypoints, got {len(rows)}"
    return np.array(rows, dtype=np.float32)


